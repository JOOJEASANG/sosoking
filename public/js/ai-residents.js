export function getAiResident(id) {
  return AI_RESIDENTS.find(resident => resident.id === id) || null;
}

export function getPublicAiResidents() {
  return AI_RESIDENTS
    .filter(resident => resident.hiddenFromPublicList !== true)
    .map(({ id, name, emoji, role, age, job, mbti, specialty, catchphrases, likes }) => ({
      id,
      name,
      emoji,
      role,
      age,
      job,
      mbti,
      specialty,
      catchphrases,
      likes,
    }));
}

export function buildResidentPrompt(resident) {
  if (!resident) return '';
  return [
    `이름: ${resident.name}`,
    `역할: ${resident.role}`,
    `나이/직업/MBTI: ${resident.age} / ${resident.job} / ${resident.mbti}`,
    `성격: ${resident.personality}`,
    `말투: ${resident.tone}`,
    `전문분야: ${resident.specialty}`,
    `좋아하는 것: ${(resident.likes || []).join(', ')}`,
    `싫어하는 것: ${(resident.dislikes || []).join(', ')}`,
    `말버릇: ${(resident.catchphrases || []).join(' / ')}`,
    `말하기 규칙: ${(resident.speakingRules || []).join(' ')}`,
    `절대 하지 말 것: ${(resident.neverDo || []).join(', ')}`,
    `안전 기준: ${resident.safeStyle}`,
  ].join('\n');
}
