function applyVars(text, vars) {
  return text
    .replaceAll('{companyName}', vars.companyName || '소소킹 AI 수사망')
    .replaceAll('{ceoName}', vars.ceoName || '운영자')
    .replaceAll('{email}', vars.email || '운영자 이메일 미등록');
}

export async function renderPolicy(container, type = 'terms') {
  const key = TITLES[type] ? type : 'terms';
  let biz = {};
  let customText = null;
  try {
    const snap = await getDoc(doc(db, 'site_settings', 'config'));
    if (snap.exists()) {
      const d = snap.data();
      biz = d.businessInfo || {};
      customText = d.policies?.[key] || null;
    }
  } catch {}

  const text = applyVars(customText || DEFAULTS[key], biz);
  injectPolicyStyle();
  container.innerHTML = `
    <div class="policy-page">
      <div class="page-header">
        <a href="#/" class="back-btn">‹</a>
        <span class="logo">${TITLES[key]}</span>
      </div>
      <div class="container policy-container">
        <article class="policy-card">
          <pre>${escapeHtml(text)}</pre>
        </article>
      </div>
    </div>`;
}

function injectPolicyStyle() {
  if (document.getElementById('ai-hunt-policy-style')) return;
  const style = document.createElement('style');
  style.id = 'ai-hunt-policy-style';
  style.textContent = `
    .policy-page { min-height:100vh; background:#05070d; color:#ecf6ff; }
    .policy-container { padding-top:18px; padding-bottom:88px; }
    .policy-card { border:1px solid rgba(102,234,255,.18); border-radius:18px; padding:18px; background:rgba(255,255,255,.04); }
    .policy-card pre { margin:0; white-space:pre-wrap; word-break:keep-all; overflow-wrap:anywhere; color:rgba(236,246,255,.78); font-family:var(--font-sans); font-size:13px; line-height:1.8; }
  `;
  document.head.appendChild(style);
}

function escapeHtml(s) {
  return String(s ?? '').replace(/&/g,'&amp;').replace(/</g,'&lt;').replace(/>/g,'&gt;');
}
