const SAVED_POLICY_NOTICE = `

[생활법정 운영 고지]
본 문서에 예전 명칭이나 표현이 일부 남아 있더라도, 본 서비스는 현재 "소소킹 생활법정"이라는 오락용 AI 모의법정 서비스로 운영됩니다. 서비스 내 원고·피고·판사·판결·재판 등의 표현은 실제 법적 절차가 아닌 역할극 및 엔터테인먼트 연출입니다. AI 판결은 실제 법적 효력이 없으며, 실제 법률 문제는 반드시 전문가 또는 관계 기관에 상담해야 합니다.`;

function normalizePolicyText(text) {
  return String(text || '')
    .replace(/소소킹 토론배틀/g, '소소킹 생활법정')
    .replace(/토론배틀/g, '생활법정')
    .replace(/토론 배틀/g, '생활법정')
    .replace(/AI 심판/g, 'AI 판사')
    .replace(/심판/g, '판사')
    .replace(/판정/g, '판결')
    .replace(/배틀/g, '재판')
    .replace(/주제/g, '사건')
    .replace(/A팀/g, '원고')
    .replace(/B팀/g, '피고')
    .replace(/찬성/g, '원고')
    .replace(/반대/g, '피고');
}

function applyBiz(text, biz, fromSavedPolicy = false) {
  const normalized = normalizePolicyText(text) + (fromSavedPolicy ? SAVED_POLICY_NOTICE : '');
  return normalized
    .replace(/{companyName}/g, biz.companyName || '소소킹 생활법정 운영팀')
    .replace(/{ceoName}/g, biz.ceoName || '')
    .replace(/{businessNumber}/g, biz.businessNumber || '')
    .replace(/{contact}/g, biz.contact || '')
    .replace(/이메일: {email}\n/g, biz.email ? `이메일: ${biz.email}\n` : '이메일: 운영자 연락처를 통해 문의해주세요.\n')
    .replace(/{email}/g, biz.email || '운영자 연락처를 통해 문의해주세요.')
    .replace(/{address}/g, biz.address || '');
}

